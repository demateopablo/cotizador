// Funci√≥n para cargar productos
function loadProducts(filter = "") {
    productGrid.innerHTML = "";
    let filter_lowerCase=filter.toLowerCase();
    // Cargar sembradoras
    data.sembradoras.forEach(product => {
        product.versiones.forEach(version => {
            if (
                product.nombre.toLowerCase().includes(filter_lowerCase) ||
                version.codigo.toString().toLowerCase().includes(filter_lowerCase) ||
                version.chasis?.toString().toLowerCase().includes(filter_lowerCase) ||
                version.version.toString().toLowerCase().includes(filter_lowerCase) ||
                version.lineas_separacion?.toLowerCase().includes(filter_lowerCase) ||
                version.capacidad?.toLowerCase().includes(filter_lowerCase)
            ) {
                productGrid.appendChild(createProductCard(product, version));
            }
        });
    });

    // Agregar eventos a los t√≠tulos despu√©s de cargar las cards
    agregarEventosCards();

    // Cargar opcionales seg√∫n el filtro
    loadOptionals(filter);
}

// Funci√≥n para cargar opcionales (filtrados o disponibles)
function loadOptionals(filter = "") {
    const optionalsGrid = document.getElementById("optionals-grid");
    optionalsGrid.innerHTML = "";

    let optionalsToLoad = [];

    if (filter) {
        // Filtrar opcionales por b√∫squeda
        optionalsToLoad = data.opcionales.filter(opcional =>
            opcional.nombre.toLowerCase().includes(filter.toLowerCase()) ||
            opcional.id.toString().includes(filter)
        );
    } else if (availableOptionals.length > 0) {
        // Cargar opcionales disponibles en el carrito
        optionalsToLoad = data.opcionales.filter(opc => availableOptionals.includes(opc.id));
    }

    // Agregar tarjetas al DOM
    optionalsToLoad.forEach(opcional => {
        optionalsGrid.appendChild(createOptionalCard(opcional));
    });
}


// A√±adir producto al carrito
function addToCart(productName, productCode, price, quantity, opcionales = [], productDescription) {
    if (quantity < 1) {
        alert(`La cantidad de ${productName} c√≥digo ${productCode} debe ser mayor a cero, pero se ingres√≥ ${quantity}.`);
        return;
    }
    const existingItem = cart.find(item => item.name === productName && item.code === productCode);

    if (existingItem) {
        existingItem.quantity = Number(existingItem.quantity) + Number(quantity);
    } else {
        cart.push({ name: productName, code: productCode, price: price, quantity: quantity, description: productDescription });
        // Agregar opcionales a la lista de disponibles si es una sembradora
        if (opcionales.length > 0) {
            availableOptionals = opcionales;
            loadOptionals();
        }
    }
    updateCart();
    showToast(`‚úÖ ${productCode} a√±adido al carrito`);
}

// Actualizar el carrito
function updateCart() {
    cartItems.innerHTML = "";
    let total = 0;

    cart.forEach(item => {
        const li = document.createElement("li");
        li.innerHTML = `
            <span><b>${item.name}</b> (C√≥d: ${item.code}) (x${item.quantity})</span>
            <span>$${(item.price * item.quantity).toLocaleString('es-AR', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}</span>
            <button title="Remover producto del Carrito de Cotizaci√≥n" onclick="removeFromCart('${item.name}', '${item.code}')">‚ùå</button>
        `;
        cartItems.appendChild(li);
        total += item.price * item.quantity;
    });

    totalElement.textContent = `$${total.toLocaleString('es-AR', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}

// Eliminar producto del carrito
function removeFromCart(productName, productCode) {
    cart = cart.filter(item => !(item.name === productName && item.code === productCode));
    updateCart();
    showToast(`‚ùå ${productCode} removido del carrito`, true);
}

